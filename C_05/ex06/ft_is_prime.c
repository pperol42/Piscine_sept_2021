int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb < 2)
		return (0);
	while (i <= nb / i)
	{
		if (nb % i == 0)
			return (0);
		else if (i == 2)
			i++;
		else
			i += 2;
	}
	return (1);
}
